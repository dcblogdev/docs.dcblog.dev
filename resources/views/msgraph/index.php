<h1 class="docs-heading">Laravel Microsoft Graph API</h1>
<section class="docs-intro">
    
    <p><b>A Laravel package for working with Microsoft Graph API.</b></p>
    <p><a href="https://github.com/daveismyname/laravel-microsoft-graph">https://github.com/daveismyname/laravel-microsoft-graph</a></p>

    <p>MsGraph comes in two flavours:</p>

    <ul>
        <li>MsGraph: login in as a user.</li>
        <li>MsGraphAdmin: login as a tenant (administrator) useful for running background tasks.</li>
    </ul>

    <p>Microsoft Graph API documentation can be found at:<br>
    <a href="https://developer.microsoft.com/en-us/graph/docs/api-reference/beta/beta-overview">https://developer.microsoft.com/en-us/graph/docs/api-reference/beta/beta-overview</a></p>

</section><!--//docs-intro-->
